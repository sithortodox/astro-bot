def generate_keywords(name_ru: str, arcana_type: str, is_reversed: bool = False) -> list[str]:
    if arcana_type == "major" and name_ru in KEYWORDS["major"]["upright"]:
        key = "reversed" if is_reversed else "upright"
        return KEYWORDS["major"][key].get(name_ru, ["таро", "предсказание"])
    return ["таро", "предсказание", "раковина"]


def parse_major_arcana():
    cards = []
    for card_id, name_en, name_ru, number in MAJOR_ARCANA:
        card = {
            "id": card_id,
            "name": name_en,
            "name_ru": name_ru,
            "arcana_type": "major",
            "number": number,
            "suit": None,
            "upright_meaning": f"Карта {name_ru} символизирует силу и трансформацию.",
            "reversed_meaning": f"Перевёрнутый {name_ru} указывает на внутренние блоки и сопротивление.",
            "keywords": generate_keywords(name_ru, "major"),
            "description": f"Карта Мажорного Арканала {name_ru} (номер {number}) — одна из 22 ключевых карт колоды Таро.",
            "love": "Влияет на романтические отношения и эмоциональные связи.",
            "career": "Указывает на профессиональные возможности и вызовы.",
            "finance": "Символизирует финансовую ситуацию и возможности.",
        }
        cards.append(card)
    return cards


def parse_minor_arcana():
    cards = []
    for suit_en, (suit_ru, card_list) in MINOR_ARCANA.items():
        for i, (name_en, name_ru) in enumerate(card_list):
            card_id = f"minor_{suit_en}_{i:02d}_{name_en.lower()}"
            card = {
                "id": card_id,
                "name": f"{name_en} of {suit_en.title()}",
                "name_ru": f"{name_ru} {suit_ru}",
                "arcana_type": "minor",
                "number": i,
                "suit": suit_en,
                "upright_meaning": f"Карта {name_ru} {suit_ru} символизирует энергию и действие в этой сфере.",
                "reversed_meaning": f"Перевёрнутый {name_ru} {suit_ru} указывает на задержки и препятствия.",
                "keywords": ["таро", suit_ru.lower(), name_ru.lower()],
                "description": f"Карта Младшего Арканала {name_ru} {suit_ru} отражает повседневные аспекты жизни.",
                "love": "Влияет на романтические отношения и эмоциональные связи.",
                "career": "Указывает на профессиональные возможности и вызовы.",
                "finance": "Символизирует финансовую ситуацию и возможности.",
            }
            cards.append(card)
    return cards


def main():
    major = parse_major_arcana()
    minor = parse_minor_arcana()

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    with open(OUTPUT_DIR / "tarot_major.json", "w", encoding="utf-8") as f:
        json.dump(major, f, ensure_ascii=False, indent=2)
    print(f"Saved {len(major)} major arcana cards")

    with open(OUTPUT_DIR / "tarot_minor.json", "w", encoding="utf-8") as f:
        json.dump(minor, f, ensure_ascii=False, indent=2)
    print(f"Saved {len(minor)} minor arcana cards")


if __name__ == "__main__":
    main()
