def generate_minor_arcana():
    cards = []
    number_names = {
        0: "Ace", 1: "Two", 2: "Three", 3: "Four", 4: "Five",
        5: "Six", 6: "Seven", 7: "Eight", 8: "Nine", 9: "Ten",
        10: "Page", 11: "Knight", 12: "Queen", 13: "King",
    }

    for suit_en, suit_data in MINOR_ARCANA_DATA.items():
        for i, (name_en, name_ru, description) in enumerate(suit_data["cards"]):
            card_id = f"minor_{suit_en}_{i:02d}_{name_en.lower()}"

            is_reversed = False
            upright = description
            reversed_text = f"Перевёрнутый {name_ru} {suit_data['suit_ru']} указывает на задержки, препятствия или нереализованный потенциал в области: {suit_data['theme']}."

            keywords = [suit_data["suit_ru"].lower(), suit_data["element"].lower(), name_ru.lower()]
            if i < 10:
                keywords.append("число")
            else:
                keywords.append("двор")

            card = {
                "id": card_id,
                "name": f"{name_en} of {suit_en.title()}",
                "name_ru": f"{name_ru} {suit_data['suit_ru']}",
                "arcana_type": "minor",
                "number": i,
                "suit": suit_en,
                "upright_meaning": upright,
                "reversed_meaning": reversed_text,
                "keywords": keywords,
                "description": f"Карта {name_ru} {suit_data['suit_ru']} относится к стихии {suit_data['element']} и символизирует {suit_data['theme']}.",
                "love": f"Влияние {suit_data['suit_ru']} на романтические отношения: {suit_data['theme']}.",
                "career": f"Влияние {suit_data['suit_ru']} на профессиональную сферу: {suit_data['theme']}.",
                "finance": f"Влияние {suit_data['suit_ru']} на финансовую сферу: {suit_data['theme']}.",
            }
            cards.append(card)

    return cards


